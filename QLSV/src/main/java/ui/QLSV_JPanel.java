    private void tbl_sinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_sinhVienMouseClicked
        try {
            int row = tbl_sinhVien.getSelectedRow();
            if (row >= 0) {
                String id = (String) tbl_sinhVien.getValueAt(row, 0);
                SinhVienDAO svDao = new SinhVienDAO();

                SinhVien sv = svDao.TimKiemSinhVien(id);

                if (sv != null) {
                    txt_maSinhVien.setText(sv.getMaSinhVien());
                    txt_hoVaTen.setText(sv.getHoVaTen());
                    txt_email.setText(sv.getEmail());
                    txt_soDienThoai.setText(sv.getSoDienThoai());
                    txa_diaChi.setText(sv.getDiaChi());
                    btn_nam.setSelected(sv.getGioiTinh() == 1 ? true : false);
                    
                    // Xử lý hiên thị hình đại diện khi click vào bảng Asg.P7:23/08/2022
                    if (sv.getHinhDaiDien() != null) {
                        Image img = AnhDaiDien.TaoAnhFormByteArray(sv.getHinhDaiDien(), "jpg");
                        lbl_anhDaiDien.setIcon(new ImageIcon(img));
                        anhDaiDien = sv.getHinhDaiDien();

                    } else {
                        anhDaiDien = sv.getHinhDaiDien();
                        ImageIcon icon = new ImageIcon(getClass().
                                getResource("/icon/10207-man-student-light-skin-tone-icon-64.png"));
                        lbl_anhDaiDien.setIcon(icon);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(gdc, e.getMessage(), "Lỗi tbl_sinhVienMouseClicked");
        }
    }//GEN-LAST:event_tbl_sinhVienMouseClicked

    private void btn_moHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_moHinhActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".jpg");
                }
            }

            @Override
            public String getDescription() {
                return "Image File (*.jpg)";
            }
        });
        if (chooser.showOpenDialog(gdc) == JFileChooser.CANCEL_OPTION) {
            return;
        }
        File file = chooser.getSelectedFile();
        try {
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = AnhDaiDien.ThayDoiKichThuoc(icon.getImage(), 140, 160);
            ImageIcon thayDoiKichThuocIcon = new ImageIcon(img);
            lbl_anhDaiDien.setIcon(thayDoiKichThuocIcon);

            anhDaiDien = AnhDaiDien.toByteArray(img, "jpg");

        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(gdc, e.getMessage(), "Lỗi btn_moHinhActionPerformed");
        }

    }//GEN-LAST:event_btn_moHinhActionPerformed


