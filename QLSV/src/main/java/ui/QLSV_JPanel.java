    /*
        Ngày 21/08/2022
        Chức năng lấy dữ liệu từ csdl lên cho bảng ở QLSV_Panel
     */
    private void LayDuLieuChoBang() {
        try {
            SinhVienDAO svDao = new SinhVienDAO();
            List<SinhVien> list = svDao.TimKiemTatCaSinhVien(); // Đọc all data

            tblModel.setRowCount(0); // Xóa các data hiện đang hiển thị trong bảng

            for (SinhVien it : list) { // Duyệt qua các sinhVien có trong ds trả về
                tblModel.addRow(new Object[]{ // Tạo dữ liệu đầu vào trong bảng
                    it.getMaSinhVien(), it.getHoVaTen(), it.getEmail(), it.getSoDienThoai(),
                    it.getGioiTinh() == 1 ? "Nam" : "Nữ", it.getDiaChi()
                });
            }
            tblModel.fireTableDataChanged(); // Đảm báo dữ liệu sau khi thay đổi sẻ đươc hiển thị
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(gdc, e.getMessage(), "Lỗi lấy dữ liệu cho bảng");
        }
    }

