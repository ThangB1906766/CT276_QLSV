    private void btn_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timKiemActionPerformed
       try {
            BangDiemDAO bdDao = new BangDiemDAO();
            BangDiem bd = bdDao.TimKiemBangDiem(txt_timKiem_maSinhVien.getText());
            
            if(bd != null){
                txt_maSinhVien.setText(bd.getMaSinhVien());
                txt_tiengAnh.setText(String.format("%.2f", bd.getTiengAnh()));
                txt_tinHoc.setText(String.format("%.2f", bd.getTinHoc()));
                txt_theDuc.setText(String.format("%.2f", bd.getTheDuc()));
                
                txt_maSinhVienFocusLost(null);
                txt_tiengAnhFocusLost(null);
            }else{
                ThongBao.ThongBao(gDChinh, "Không tìm thấy mã sinh viên", "Thông báo");
            }
        } catch (Exception e) {
             e.printStackTrace();
            ThongBao.ThongBaoLoi(gDChinh, e.getMessage(), "Lỗi Exception e");
        }
    }//GEN-LAST:event_btn_timKiemActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        try {
            if(txt_maSinhVien.getText().equals("")){
                ThongBao.ThongBao(gDChinh, "Cần phải nhập mã sinh viên", "Lỗi");
                return;
            }
            
            BangDiemDAO bdDao = new BangDiemDAO();
            BangDiem bd = bdDao.TimKiemBangDiem(txt_maSinhVien.getText());
            
            if(bd == null){
                ThongBao.ThongBao(gDChinh, "Mã sinh viên không tồn tại trong bảng điểm", "Lỗi");
                return;
            }
            
            if(ThongBao.ThongBaoXacNhan(gDChinh, "Bạn có muốn xóa điểm của sinh viên này không?", "Hỏi")
                    == JOptionPane.NO_OPTION){
                return;
            }
            
            if(bdDao.Xoa(txt_maSinhVien.getText())){
                ThongBao.ThongBao(gDChinh, "Điểm của sinh viên đã được xóa thành công", "Thông báo");
            }else{
                ThongBao.ThongBao(gDChinh, "Không thể xóa điểm của sinh viên", "Thông báo");
            }
                
              TaiBangDiem();
        } catch (Exception e) {
             e.printStackTrace();
            ThongBao.ThongBaoLoi(gDChinh, e.getMessage(), "Lỗi Exception e");
        }
    }//GEN-LAST:event_btn_xoaActionPerformed


