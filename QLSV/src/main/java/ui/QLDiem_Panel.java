    public void TaiBangDiem(){
        try {
            BangDiemDAO bdDao = new BangDiemDAO();
            List<BangDiem> list =bdDao.TimKiemDiemCaoNhat(3);
            tblModel.setRowCount(0);
            
            for (BangDiem bd : list) {
                tblModel.addRow(new Object[]{
                    bd.getMaSinhVien(), bd.getTiengAnh(), bd.getTinHoc(), bd.getTheDuc(),
                    (bd.getTiengAnh() + bd.getTinHoc() + bd.getTheDuc())/3
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(gDChinh, e.getMessage(), "Lỗi Exception e");
        }
    }

