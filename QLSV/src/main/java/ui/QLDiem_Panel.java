    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
        StringBuilder sb = new StringBuilder();
        KiemTraDuLieuNhapVao.KiemTraRong(txt_maSinhVien, sb, "Mã sinh viên cần phải nhập");
        KiemTraDuLieuNhapVao.KiemTraRong(txt_tiengAnh, sb, "Tiếng anh cần phải nhập");
        KiemTraDuLieuNhapVao.KiemTraRong(txt_tinHoc, sb, "Tin học cần phải nhập");
        KiemTraDuLieuNhapVao.KiemTraRong(txt_theDuc, sb, "Thể dục cần phải nhập");
        // KiemTraDuLieuNhapVao.KiemTraRong(txt_hoVaTen, sb, "Họ và tên cần phải nhập");
        // Cần kiểm tra điểm nhập vào >0 <10
        if (sb.length() > 0) {
            ThongBao.ThongBaoLoi(gDChinh, sb.toString(), "Lỗi btn_luuActionPerformed");
            return;
        }
        try {

            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txt_maSinhVien.getText());
            bd.setTiengAnh(Float.parseFloat(txt_tiengAnh.getText()));
            bd.setTinHoc(Float.parseFloat(txt_tinHoc.getText()));
            bd.setTheDuc(Float.parseFloat(txt_theDuc.getText()));

            BangDiemDAO bdDao = new BangDiemDAO();
           
            /*
                Ngày 22/08/2022
                Chức năng cập nhật sinh viên khi nguòi dùng nhấn cập nhật mà không bị trùng lắp data
            */
            if (bdDao.TimKiemBangDiem(txt_maSinhVien.getText()) != null) {
                
                if(ThongBao.ThongBaoXacNhan(gDChinh, "Bạn có muốn cập nhật bảng điểm không?", "Hỏi")== JOptionPane.NO_OPTION){
                    return;
                }
                
                if (bdDao.CapNhat(bd)) {
                    ThongBao.ThongBao(gDChinh, "Bảng điểm đã được cập nhật", "Thông báo");
                } else {
                    ThongBao.ThongBao(gDChinh, "Không cập nhật bảng điểm", "Thông báo");
                }

            } else {
                if (bdDao.Them(bd)) {
                    ThongBao.ThongBao(gDChinh, "Bảng điểm đã được lưu", "Thông báo");
                } else {
                    ThongBao.ThongBao(gDChinh, "Không thể lưu bảng điểm", "Thông báo");
                }
            }
             TaiBangDiem();
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(gDChinh, e.getMessage(), "Lỗi Exception e");
        }
    }//GEN-LAST:event_btn_luuActionPerformed

